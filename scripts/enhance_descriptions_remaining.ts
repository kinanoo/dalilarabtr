async function enhance() {
    console.log('Enhancing remaining code descriptions...');
    console.log(`Total codes to update: ${Object.keys(enhancedDescriptions).length}`);

    let success = 0;
    let failed = 0;
    const errors: string[] = [];

    for (const [code, description] of Object.entries(enhancedDescriptions)) {
        const { error } = await supabase
            .from('security_codes')
            .update({ description })
            .eq('code', code);

        if (error) {
            failed++;
            errors.push(`${code}: ${error.message}`);
        } else {
            success++;
        }
    }

    console.log(`\nResults:`);
    console.log(`  Updated: ${success}`);
    console.log(`  Failed: ${failed}`);

    if (errors.length > 0) {
        console.log(`\nErrors:`);
        errors.forEach(e => console.log(`  ${e}`));
    }

    // Final count
    const { count } = await supabase
        .from('security_codes')
        .select('*', { count: 'exact', head: true });

    console.log(`\nAll ${count} codes now have enhanced descriptions.`);
}

enhance();
