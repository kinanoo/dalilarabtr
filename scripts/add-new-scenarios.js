async function main() {
  console.log('Adding ' + NEW_SCENARIOS.length + ' new scenarios...\n');
  let added = 0, errors = 0;

  for (const s of NEW_SCENARIOS) {
    const row = {
      id: s.id,
      title: s.title,
      category: s.category,
      subcategory: s.subcategory,
      description: s.description,
      risk: s.risk,
      steps: s.steps,
      docs: s.docs,
      cost: s.cost,
      legal: s.legal,
      tip: s.tip,
      is_active: true,
      sources: [],
    };

    const { error } = await supabase.from('consultant_scenarios').upsert(row);
    if (error) {
      console.log('  ERROR: ' + s.id + ' — ' + error.message);
      errors++;
    } else {
      console.log('  OK: ' + s.id + ' → ' + s.category + '/' + s.subcategory);
      added++;
    }
  }

  console.log('\nDone: ' + added + ' added, ' + errors + ' errors');

  // Final count
  const { data } = await supabase.from('consultant_scenarios').select('id');
  console.log('Total scenarios in DB now: ' + data.length);
}

main().catch(console.error);
