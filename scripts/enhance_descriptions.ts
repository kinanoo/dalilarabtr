async function enhance() {
    console.log('Enhancing security code descriptions...');
    console.log(`Total codes to update: ${Object.keys(enhancedDescriptions).length}`);

    let success = 0;
    let failed = 0;
    const errors: string[] = [];

    for (const [code, description] of Object.entries(enhancedDescriptions)) {
        const { error } = await supabase
            .from('security_codes')
            .update({ description })
            .eq('code', code);

        if (error) {
            failed++;
            errors.push(`${code}: ${error.message}`);
        } else {
            success++;
        }
    }

    console.log(`\nResults:`);
    console.log(`  Updated: ${success}`);
    console.log(`  Failed: ${failed}`);

    if (errors.length > 0) {
        console.log(`\nErrors:`);
        errors.forEach(e => console.log(`  ${e}`));
    }

    // Verify samples
    const { data: samples } = await supabase
        .from('security_codes')
        .select('code, description')
        .in('code', ['Ç113', 'V137', 'G87', 'V164', 'N120'])
        .limit(5);

    if (samples) {
        console.log('\nSample verification:');
        samples.forEach(s => {
            console.log(`  ${s.code}: ${s.description?.slice(0, 80)}...`);
        });
    }
}

enhance();
