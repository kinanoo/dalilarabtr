-- ============================================================================
-- حذف الأكواد المفبركة من security_codes — دفعة 1ب (قرارك — اقرأ قبل التشغيل)
-- ============================================================================
-- التدقيق (2026-07-09، باحثان مستقلان عن بعضهما) لم يجد أي مصدر قانوني تركي
-- يوثّق وجود «عائلة N-XX (إذن)» التالية — وهي فوق ذلك ورثت نفس الشروحات
-- المخلوطة لعائلة G. إبقاء أكواد قانونية غير موجودة أخطر من حذفها:
-- قارئ قد يعتقد أن على كوده «نسخة إذن» لا وجود لها.
--   N26, N34, N42, N43, N48, N58, N64, N65, N66, N78, N87
-- وكذلك G55 («فساد مالي») — لا وجود له في أي مصدر.
--
-- هذا الملف يحذف 12 صفاً نهائياً. إن أردت الاحتفاظ بها مؤقتاً بدل الحذف،
-- لا تشغّل هذا الملف وأخبرني لأجهّز بديلاً يخفيها (إن وُجد عمود إخفاء).
-- ============================================================================

DELETE FROM public.security_codes
WHERE code IN ('N26','N34','N42','N43','N48','N58','N64','N65','N66','N78','N87','G55');
