<h2>كم تستغرق؟ (المدة الواقعية)</h2>
<p>لا توجد مدة قانونية ثابتة، ولا يستطيع أحد بصدق أن يعطيك تاريخاً. المدة تختلف اختلافاً واسعاً حسب نوع الملف وحالتك وسرعة ردّ الأجهزة الأمنية، ومرحلة البحث الأمني والأرشيفي هي دائماً الأطول. تتداول بعض الشروح تقديرات غير رسمية تتراوح من بضعة أشهر لملفات معيّنة إلى نحو سنة أو أكثر (وأحياناً سنة إلى سنتين) لملفات أخرى — لكنها <strong>مجرد متوسّطات غير ملزِمة</strong>، قد تكون في حالتك أقصر أو أطول بكثير. تعامل مع أي رقم على أنّه تقدير لا وعد، ولا تبنِ التزامات مالية أو حياتية على موعد مفترض لم تصدره جهة رسمية.</p>

<h2>ما هو التجنيس الاستثنائي؟</h2>
<p>«التجنيس الاستثنائي» (İstisnai vatandaşlık) مسارٌ منصوص عليه في المادة 12 من القانون رقم 5901 (ولائحته التنفيذية)، ويُسقِط شروط الإقامة والمدّة المعتادة عن فئات محدّدة: من تَعُدّهم الدولة ذوي قيمة لها (في مجالات صناعية أو علمية أو اقتصادية أو ثقافية أو اجتماعية أو رياضية)، وبعض المستثمرين ضمن حدود مقرّرة، وحاملي «البطاقة التركوازية» (Turkuaz) وأزواجهم وأولادهم القُصَّر، ومن ترى الدولة في تجنيسهم مصلحة وطنية. عملياً، جُنِّس عددٌ كبير من السوريين عبر هذا المسار، ما يؤكّد أنّه الطريق الواقعي لكثيرين — لكنّه مسار حسّاس وتقديري، وقد تُراجَع سياساته أو تتغيّر. النقطة الأهم: استيفاء المعايير <strong>لا يمنحك الجنسية تلقائياً</strong>؛ فهي تُمنح بتقدير الدولة وتُختم بقرار رئاسي، وحتى الملف النظيف المستوفي قد يتأخّر أو لا يُوافَق عليه.</p>

<h2>لا ضمان بالموافقة — وتحذير من السماسرة</h2>
<p>لأنّ النتيجة تعتمد على تحقيق أمني وقرار رئاسي تقديري، فإنّ <strong>أحداً لا يستطيع أن يضمن لك الجنسية، ولا الموافقة، ولا مدة سريعة أو محدّدة</strong>. من يَعِدك بجنسية «مضمونة» أو «فورية» مقابل المال يضلّلك؛ فلا أحد يستطيع شراء نتيجة أمنية لصالحك. احذر تحديداً من:</p>
<ul>
<li>دفع مبالغ مقابل وعد بموافقة مؤكّدة أو تسريع مضمون — هذا خارج قدرة أي وسيط.</li>
<li>تسليم رقم طلبك أو بيانات هويتك أو بطاقتك لروابط «متابعة» غير رسمية — وهي وسيلة احتيال موثّقة تنتحل صفة مديرية النفوس.</li>
<li>الاعتماد على أي رابط أو موقع أرسله لك سمسار بدل القنوات الحكومية الرسمية.</li>
</ul>
<p>تحقّق من حالتك فقط عبر المديرية التي استلمت ملفك أو بوابة e-Devlet الرسمية، ولا تدفع لأي طرف يبيعك «ضماناً».</p>

