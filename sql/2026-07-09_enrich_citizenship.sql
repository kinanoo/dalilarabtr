<ul>
<li><strong>المديرية التي استلمت طلبك:</strong> استفسر مباشرةً من مديرية النفوس والأحوال المدنية في ولايتك (İl Nüfus ve Vatandaşlık Müdürlüğü) التي قدّمت لها الملف، مستنداً إلى رقم الطلب الذي أُعطي لك عند التقديم. هذه أوثق جهة لمعرفة موقع ملفك.</li>
<li><strong>بوابة e-Devlet الرسمية:</strong> من خلال بوابة الحكومة (turkiye.gov.tr) ضمن خدمات المديرية العامة للنفوس والجنسية، تتوفّر خدمة للاستعلام عن حالة طلب الجنسية تحتاج عادةً رقم الطلب و/أو بيانات هويتك. ادخل دائماً من الموقع الرسمي مباشرةً، وابحث عن «Nüfus ve Vatandaşlık İşleri» داخل البوابة، ولا تعتمد على أي رابط يصلك عبر رسالة أو وسيط.</li>
