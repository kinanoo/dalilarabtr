<h2>التأشيرة الإلكترونية (e-Vize)</h2>
<p>أُطلقت التأشيرة الإلكترونية في 17 أبريل 2013 من وزارة الخارجية التركية، ويُتقدَّم لها حصراً عبر البوابة الرسمية <a href="https://www.evisa.gov.tr/" target="_blank" rel="noopener">evisa.gov.tr</a> — لا عبر أي موقع وسيط. تدخل بياناتك وبيانات جوازك وتاريخ سفرك، تدفع إلكترونياً، وتصلك التأشيرة بالبريد الإلكتروني.</p>
<h3>مَن هو المؤهّل؟</h3>
<p>الجنسيات العربية المؤهّلة للتأشيرة الإلكترونية هي عادةً غير المعفاة: مصر، فلسطين، اليمن، العراق، ليبيا، الجزائر. لكن الأهلية غالباً <strong>مشروطة</strong>: يُشترط في كثير من الحالات أن يحمل المتقدّم تأشيرة أو إقامة سارية من دول شنغن أو الولايات المتحدة أو بريطانيا أو أيرلندا، وأحياناً السفر عبر شركة طيران محددة، مع حجز فندقي وتذكرة عودة وإثبات مالي كافٍ. المنح النموذجي: 30 يوماً بدخول واحد، وتُذكر تأشيرة العراق الإلكترونية بأنها تُصدر مجاناً. هذه الشروط تتغيّر، والبوابة الرسمية صارت تعرضها فقط داخل مسار التقديم — فتعامل معها كشرط شائع يجب تأكيده، لا كقاعدة قطعية.</p>
<h3>خطوات التقديم</h3>
<ul>
  <li>ادخل إلى <a href="https://www.evisa.gov.tr/" target="_blank" rel="noopener">evisa.gov.tr</a> واختر بلد جواز سفرك ونوع وثيقة السفر.</li>
  <li>اطّلع على شروط الأهلية التي تظهر لجنسيتك تحديداً — إن ظهر أنك غير مؤهّل، فطريقك هو تأشيرة السفارة.</li>
  <li>أدخل بياناتك الشخصية وبيانات الجواز وتاريخ الوصول.</li>
  <li>ادفع الرسم إلكترونياً بالبطاقة، واحفظ التأشيرة التي تصلك بالبريد واطبعها.</li>
</ul>
<p><strong>الرسوم:</strong> لا نذكر رقماً ثابتاً — الرسم يُحدَّد حسب الجنسية والغرض ويتغيّر، ويُعرض فقط داخل مسار التقديم على البوابة. تحقّق من الرسم الخاص بجنسيتك على evisa.gov.tr قبل الدفع، ولا تدفع لأي وسيط يزعم «تسريع» التأشيرة.</p>

<h2>تأشيرة السفارة (الملصقة)</h2>
<p>مَن ليس معفياً ولا مؤهّلاً للتأشيرة الإلكترونية — ويشمل ذلك السوريين الذين يحتاجون تأشيرة سفارة دون إعفاء، أو من لم تنطبق عليه شروط e-Vize — عليه استخراج تأشيرة ملصقة من القنصلية التركية قبل السفر. الأوراق المعتادة:</p>
<ul>
  <li>جواز سفر ساري المفعول 60 يوماً على الأقل بعد انتهاء مدة التأشيرة.</li>
  <li>استمارة الطلب وصور بيومترية حديثة.</li>
  <li>تذكرة ذهاب وعودة وحجز إقامة/فندق.</li>
  <li>إثبات مالي كافٍ وتأمين صحي للسفر.</li>
  <li>أوراق تخص الغرض: دعوة، عقد عمل، قبول جامعي، وثائق عائلية… حسب نوع الفيزا.</li>
</ul>

<h2>أنواع الفيزا حسب الغرض</h2>
<h3>السياحة والزيارة</h3>
<p>إقامة قصيرة عبر الإعفاء أو التأشيرة الإلكترونية أو الملصقة، وتغطي السياحة والزيارة العائلية القصيرة.</p>
<h3>الأعمال (Business)</h3>
<p>لحضور اجتماعات ومؤتمرات وصفقات — إقامة قصيرة، وقد تتطلب خطاب دعوة من جهة تركية.</p>
<h3>العمل (çalışma izni)</h3>
<p>تأشيرة العمل تُستخرج مقترنةً بتصريح عمل يصدره وزارة العمل؛ صاحب العمل هو من يتقدّم بالطلب، ثم يحصل العامل على تأشيرة العمل من القنصلية ويُسجّل للإقامة بعد الوصول. <strong>لا يجوز العمل قانوناً بتأشيرة سياحية.</strong></p>
<h3>الدراسة (öğrenci)</h3>
<p>تأشيرة دراسة مرتبطة بقبول جامعي، تُحوَّل بعد الوصول إلى إقامة طالب (öğrenci ikamet).</p>
<h3>لمّ الشمل العائلي</h3>
<p>للالتحاق بزوج/زوجة أو أسرة مقيمة أو تحمل الجنسية التركية، بوثائق تثبت صلة القرابة.</p>
<h3>العبور (Transit)</h3>
<p>للمرور عبر الأراضي التركية في طريقك إلى بلد ثالث.</p>
<h3>العلاج (Medical)</h3>
<p>لأغراض الاستشفاء والعلاج في مستشفى تركي، بوثائق تثبت الموعد الطبي.</p>

<h2>الفيزا ليست إقامة</h2>
<p>هذه أهم نقطة في المقال: التأشيرة (أو الإعفاء أو e-Vize) تمنحك <strong>الدخول والإقامة القصيرة فقط</strong> — بحد أقصى 90 يوماً خلال 180 يوماً — ولا تمنحك حق الإقامة الطويلة. تنص وزارة الخارجية على أنك بتأشيرة سارية لا تحتاج إقامة حتى 90 يوماً؛ أما إذا أردت البقاء أطول أو العمل أو الدراسة أو الاستقرار، فيجب استخراج تصريح إقامة (ikamet izni) من إدارة الهجرة (goc.gov.tr)، والتسجيل محلياً خلال نحو شهر من الوصول. للتفاصيل الكاملة راجع دليلنا عن <a href="/residence">الإقامة في تركيا</a>، واستخدم <a href="/tools/residence-calculator">حاسبة مدة الإقامة</a> لمعرفة الأيام المتبقية لك بدقة.</p>

<h2>حدّ الـ90 يوماً خلال 180 يوماً</h2>
<p>الإقامة القصيرة — سواء بالإعفاء أو التأشيرة الإلكترونية أو الملصقة قصيرة الأمد — محدودة بـ90 يوماً كحد أقصى ضمن أي 180 يوماً متحرّكة. وهذا الحد <strong>تراكمي</strong> عبر عدة دخلات، ولا يُصفَّر بالخروج والدخول من الحدود («فيزا ران»). تجاوزه يعني أنك في مخالفة وتحتاج إقامة للبقاء قانونياً. كما تشترط الخارجية أن يكون جواز سفرك ساري المفعول 60 يوماً على الأقل بعد انتهاء مدة التأشيرة (أي نحو 150 يوماً لتأشيرة 90 يوماً). احسب أيامك عبر <a href="/tools/residence-calculator">حاسبة مدة الإقامة</a>.</p>

<h2>تجاوز مدة الفيزا وعقوباته</h2>
<p>تجاوز المدة (vize ihlali) يستوجب غرامة إدارية <strong>إضافة إلى</strong> حظر دخول متدرّج (tahdit kodu): شهر تجاوز تقريباً يقابله حظر شهر، و3 أشهر يقابلها 3 أشهر، و6 أشهر يقابلها 6 أشهر، وحتى سنة يقابلها حظر 12 شهراً، والتجاوز الأطول قد يؤدي إلى الاحتجاز والترحيل على نفقتك. تُدفع الغرامات نقداً بالليرة التركية عادةً، وإن لم تُدفع عند الخروج أمكن دفعها خلال 10 أيام في قنصلية تركية بالخارج مما قد يرفع الحظر. رموز التقييد (مثل سلسلة V ورمز Ç-113 للدخول/الخروج غير النظامي بحظر 2–5 سنوات) تحدّد شروط عودتك. المبالغ ومدد الحظر إدارية وتتغيّر — راجع دليل <a href="/codes">رموز المنع والتقييد (Tahdit)</a> لفهم رمزك تحديداً. القانون الحاكم هو رقم 6458 (YUKK).</p>

<h2>الأخطاء الشائعة</h2>
<ul>
  <li>الاعتقاد أن التأشيرة تكفي للبقاء الطويل — هي دخول فقط، والإقامة تحتاج ikamet.</li>
  <li>الظن أن الخروج والدخول يُصفّر عدّاد الـ90 يوماً — هو تراكمي ولا يُصفَّر.</li>
  <li>الدفع لمواقع وسيطة تدّعي إصدار e-Vize — التقديم الرسمي حصراً على evisa.gov.tr.</li>
  <li>العمل بتأشيرة سياحية — مخالفة تعرّضك للغرامة والترحيل.</li>
  <li>افتراض أن رسم أو أهلية جنسية أخرى ينطبق عليك — كل جنسية مختلفة، تحقّق من جنسيتك أنت.</li>
</ul>

<h2>أسئلة شائعة</h2>
<h3>هل يستطيع السوري دخول تركيا بتأشيرة إلكترونية؟</h3>
<p>السوريون يحتاجون عادةً تأشيرة سفارة ملصقة دون إعفاء، وقد لا تنطبق عليهم شروط e-Vize. تحقّق من حالتك على evisa.gov.tr وأقرب قنصلية قبل السفر.</p>
<h3>كم رسم التأشيرة الإلكترونية؟</h3>
<p>لا يوجد رقم موحّد؛ الرسم يختلف حسب الجنسية والغرض ويُعرض فقط داخل مسار التقديم على البوابة الرسمية. لا تعتمد على رقم متداول، وتحقّق قبل الدفع.</p>
<h3>هل يمكنني العمل بتأشيرة سياحية؟</h3>
<p>لا. العمل يتطلب تأشيرة عمل مقترنة بتصريح عمل (çalışma izni)، والعمل بتأشيرة سياحية مخالفة قانونية.</p>
<h3>ماذا لو تجاوزت مدة التأشيرة بأيام؟</h3>
<p>تُفرض غرامة وقد يُسجَّل حظر دخول متدرّج حسب مدة التجاوز. راجع <a href="/codes">صفحة الرموز</a> وسدّد الغرامة قبل الخروج إن أمكن.</p>
